namespace Koturn.VRChat.Log.Enums
{
    /// <summary>
    /// Log levels in VRChat log file.
    /// </summary>
    public enum VRCLogLevel
    {
        /// <summary>
        /// Represents unknown log level.
        /// </summary>
        Other,
        /// <summary>
        /// Represents log level: "Debug".
        /// </summary>
        Debug,
        /// <summary>
        /// Represents log level: "Warning".
        /// </summary>
        Warning,
        /// <summary>
        /// Represents log level: "Error".
        /// </summary>
        Error,
        /// <summary>
        /// Represents log level: "Exception".
        /// </summary>
        Exception
    }
}
